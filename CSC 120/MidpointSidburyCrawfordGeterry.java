public class MidpointSidburyCrawfordGeterry {
	public static void main(String[] args) {
		// These are the varibles for the midpoint equation
		double x1 = 9.0;
		double x2 = 5.0;
		double y1 = 3.0;
		double y2 = 2.0;
		//This is the equation for the "x" and "y" value of the midpoint
		double midpointX= (x1 + x2) / 2;
		double midpointY= (y1 + y2) / 2;
		// This is the output for the midpoint values and statement
		System.out.print("The midpoint of (9, 3) and (5, 2) is ");
		System.out.println("("+ midpointX + "," + midpointY + ")");
		
	}
}